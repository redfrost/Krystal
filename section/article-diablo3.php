	<p>조타는 그 형체를 쫓아갔다. 도망자는 죽은 여행자들과 마찬가지로 단색 로브를 입고 있었다. 보기에는 어린아이였는데, 뛰는 모습도 서툴러 보였다. 아이는 도망가다가 나무뿌리에 걸려 비틀거리더니 나무 몸통을 들이받고 말았다. </p>
	<p>조타는 마침내 그를 덮쳐 숲 바닥으로 넘어뜨렸다. 아이는 조타의 손아귀에서 버둥거리더니 훌쩍이기 시작했다. 조타는 아이의 모자를 벗겨 냈다. 드러난 모습을 본 순간, 조타의 등을 타고 소름이 흘러내렸다.</p>
	<p>열 살도 안 되어 보이는 소년이었다. 반투명한 머리카락이 차디찬 흙 위로 흘러내렸고, 그 사이로 쥐처럼 생긴 마른 얼굴이 드러났다. 피부는 햇빛에 바래버린 백골 같았고, 눈은...</p>
