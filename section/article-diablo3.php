	<p>눈이 먼 그 소년은 교역단에 무슨 일이 일어났느냐는 조타의 질문에도 묵묵부답으로 며칠 동안이나 침묵을 지켰다. 조타는 살해된 여행자들을 정화하고 여행을 계속했다. 소년이 벙어리일지 모른다는 생각이 들었다. 그러던 어느 밤, 소년이 “엄마,”라고 잠꼬대하는 소리를 들었다.</p>
	<p>소년이 몇 차례 도망치려 하자, 조타는 장식띠 중 한 겹을 풀어 소년의 손을 묶어 끌고 다녔다. 처음부터 소년을 데리고 가겠다는 결정이 쉽지는 않았다. 소년의 모습을 보고 조타는 일종의 예감을 느꼈다. 얼마간은 혹시 악마가 소년의 모습으로 나타난 것이 아닐까도 생각했지만, 그 생각은 금세 사라졌다.  <em>‘고로라에서는 모든 것이 겉보기와 다른’ </em>법이었으니까.</p>
	<p>소년의 모습은 누가 보아도 괴상했으나, 그에게서 악마의 흔적을 감지할 수 없었다. 소년은 시력을 한 번도 가져보지 못한 이들에게서나 볼 수 있는 태도로 주위를 인지하고 있었다. 그러면서도 시시때때로 이끼 낀 바위나 튀어나온 뿌리에 걸려 넘어지기 일쑤였으며, 덕분에 조타의 걸음은 달팽이가 무색할 만큼 늦어졌다.</p>
	<p>더 큰 걱정거리는 소년의 체력이 병든 개 마냥 떨어져 간다는 사실이었다. 소년은 몇백 미터마다 멈춰 서서 숨을 골라야 했다. 새소리나 다른 짐승 소리가 주위 숲에서 메아리쳐 울리기라도 하면 소년은 매번 어린아이다운 호기심에 사로잡혀, 소리가 나는 쪽으로 발을 옮기곤 했다. 조타는 소년을 남겨둘 생각도 해보았으나, 교역단에 일어난 일에 대해 알아보고 싶은 마음에 그러지 못했다.</p>
	<p>소년은 고집스레 침묵을 고수했다. 정 원한다면 장난질에 맞춰주리라고 조타는 생각했다.</p>
	<p>“서둘러라, 악마 소년아.” 조타는 소년의 손을 묶은 줄을 당겼다.</p>
	<p>“여기는 발을 조심해라, 악마 소년아,” 조타는 바윗길을 지나면서 말했다.</p>
	<p>조타는 종일 소년을 자극했고, 소년의 얼굴에는 분노에 찬 홍조가 차올랐다. 마침내, 소년은 발끈하며, 조타가 들고 있는 줄을 확 잡아당기며 말했다. “난 악마가 아니야!”</p>
	<p>“말을 하는구나.”</p>
	<p>소년은 움찔한 기색을 보이며 고개를 떨구었다.</p>
	<p>“이름이 무엇이냐, 난 너를 돕는 사람이란다.”</p>
	<p>“거짓말. 날 속였잖아요. 다른 노래를 연주했으면서.”</p>
	<p>“속였다고? 널 그냥 두고 올 걸 그랬구나. 눈먼 아이가 고르고라에서 얼마나 오래 버틸 수 있다고 생각하느냐&mdash;“ 조타는 말을 하던 중 문득 장식끈에 집어넣어 두었던 플루트를 떠올렸다. </p>
	<p>조타는 품에서 플루트를 꺼내어 소년에게 건넸다. “그럼, 이게 네 것이냐.”</p>
	<p>소년은 손을 내밀어 더듬더니 플루트를 잡아들었다. 그리고 품 안에 고이 안았다. 소년의 눈에서 피눈물이 흘렀다. 가느다란 줄기로 내려오는 모양이 누군가가 소년의 얼굴을 예리한 칼로 그어놓은 듯 보였다.</p>
	<p>“엄마...”  아이는 낮게 속삭였다. “엄마가 플루트로 날 다시 부르겠다고 약속했어요. 노랫소리를 들었을 때, 그 노래가 아니었어요... 전혀... 엄마가 날 잊어버렸다고 생각했어요.” 소년은 조타를 바라보았다. 보이지 않는 눈이었지만 마치 조타를 보고 있는 듯했다. 화가 난 소년의 얼굴이 일그러졌다. “엄마한테 무슨 짓을 했죠?”</p>
	<p>“엄마가 야영지에 계셨다면, 이제 신들과 함께하실 거란다,” 불구덩이에 있던 머리 잘린 여인의 시신을 떠올리며, 조타가 말했다. 엉뚱한 말이나 그릇된 희망으로 진실을 덮는 건 의미 없는 일이라 생각했다. “엄마와 또 다른 어른들은 내가 거기에 도착하기 전에 운명을 맞으셨다.” </p>
	<p>“신들도 그렇게 말했어요,” 소년은 대답했다. “하지만 난 믿고 싶지 않았어요.”</p>
	<p>“살인을 저지른 그 악한 힘이 무엇이든 이제 사라지고 없다. 더는 널 괴롭히지 않을 거야.”</p>
	<p>“아뇨,” 소년은 고개를 저었다. “우리를 공격했던 악마는 아직 거기에 있어요. 야영지에 있던 어른들은 날 숲에 숨기고 악마를 속이려고 야수들을 풀었어요. 내가 거기에 없었다는 걸 알면 악마는 날 다시 찾아올 거예요. 엄마가 그랬어요. 우리 둘 다 죽기 전까지는 계속 쫓아올 거라고.”</p>
	<p>이곳의 악마는 사람을 가려 죽이지 않는다. 며칠씩이나 여행자를 뒤쫓는 일도 없지. 자, 네 이름이 무엇인지, 고향이 어디인지 말해주겠니? 고르고라에 친척은 없니?”</p>
	<p>“내 말 안 믿잖아요,” 질문에는 답하지 않은 채 소년이 말했다.</p>
	<p>그 밤, 조타는 야영지를 꾸렸다. 소년은 모닥불 가에서 플루트를 두 팔로 안고 웅크린 채 잠이 들었다. 소년의 고집은 화가 날 정도였고, 아이를 보호해줄 것이 아니라면 신들이 아이를 만나게 해 준 이유가 무엇일지 알 수 없었다. 소년은 무력했고, 외로웠고, 무서워하고 있었다...</p>
	<p>“네가 만날 사람들은 눈물과 슬픔을 보이며 네 길로부터 너를 데려가려 할 것이다. 너는 그들보다 지혜로워야 한다. 길을 벗어나서는 안 된다,” 아키예브는 그렇게 경고했었다.</p>
	<p>아키예브의 말은 지혜로웠다. 조타는 인정해야 했다. 그는 고르고라의 균형을 되찾기 위해 길을 나섰다. 고아를 돌보기 위해서가 아니었다. 그러나 조타는 아이를 저버릴 수 없었다.</p>
	<p>조타는 손가락으로 지팡이에 새겨진 글귀를 더듬었다. 지팡이 중간쯤에 있는 깊은 홈에서 손길이 멈추었다. 조타가 새긴 글귀를 망쳐 놓은 보기 싫은 흠이었다. 아키예브는 그러나 그 부분을 다듬지 못하게 했다. 그 뜻을 잊지 않게 하기 위함이었다.</p>
	<p>“무기의 강함은 네 공력에 달려 있다.” 지팡이에 흠집이 나던 날 아키예브는 말했다. 수도사들은 신성한 정의의 도구로서 몸과 마음을 연마한다. 검이나 지팡이나 다른 무기들은 사실 불필요했다. 그럼에도 수도단은 다양한 무기를 이용한 훈련과 무기 다루는 실력을 가치 있게 생각했다. 수도사가 완벽하게 균형을 이룬 공력을 공격에 집중시키기 위한 수단으로써 무기를 사용하는 일은 흔한 광경이었다. 아키예브는 그런 방법을 지지하는 편이었고 무기에 대한 자신의 철학을 조타에게 전해주는 데 수년이 넘는 시간을 들였다.</p>
	<p>“무지한 이들은 네 지팡이를 그저 나무 막대기로, 부러질 물건으로만 볼 것이다,” 아키예브는 말을 이었다. “하지만 그 지팡이는 네가 주저할 때만 부서질 것이다. 네 길을 걷는다면 그런 일은 일어나지 않는다.”</p>
	<p>조타는 스승님과 함께 수도원 벽 안쪽의 훈련장 한 곳에서 실제 무기를 들고 대련을 펼쳤다. 뭉툭한 검과 속 빈 지팡이를 휘두르며 싸우던 날들은 끝이 났다.</p>
	<p>젊은 제자는 자신감에 차 훈련장에 섰으나, 스승인 아키예브가 신월도를 꺼내어 보이자 그 자신감은 온데간데없이 사라졌다. 스승의 신월도는 그 모양만 봐서는 별로 특별할 것이 없었으나 조타는 절대 그렇지 않다는 것을 알았다. 굴하지 않는 자라 불리는 스승은 자신의 손으로 직접 신월도를 벼려내어 몇 달 동안이나 되풀이해서 강철을 구부렸다. 매일 아침, 자신의 수호신인 산의 신 자임에게 기도하며 그 검에 불굴의 힘을 불어넣었다. 단단한 바위나 강철 갑옷마저도 마치 물을 가르듯 잘라버릴 수 있는 검이었다.</p>
	<p>“무기는 장식일 뿐이다,” 아키예브는 조타의 눈에 어린 두려움을 눈치채고 말했다. “장로님들은 이 신월도가 네 지팡이만 못하다고 여기신다. 신성한 지혜에 의문을 가질 셈이냐?”</p>
	<p>“아닙니다,” 조타는 진정 믿는 듯이 답하려 했다. </p>
	<p>그리고 대련이 시작되었다. 아키예브의 첫 공격이 들어왔을 때, 조타는 의심과 망설임에 사로잡혔다. 조타가 눈앞에서 본 것은 검이 아니라 검을 든 사람이었다. 항상 자신보다 뛰어났던 자, 그 아무리 어려운 일이라도 자신에게 주어진 임무에 물러서지 않던 자였다.</p>
	<p>신월도가 조타의 지팡이를 갈랐고, 조타는 무릎을 꿇었다. 아키예브는 검을 빼내고 분개한 채 말했다. “어리석구나! 널 죽일 수도 있었다. 공포에 끌려가다니.”</p>
	<p>아키예브는 조타의 몸에 걸쳐진 초록과 파랑과 하양이 어우러진 장식띠를 불신에 찬 눈으로 바라보았다. 넌 물이 가득한 강이다... 잠잠하고 고요할 때도 있지만 때론 격랑이 되기도 하는구나.”</p>
	<p>조타의 옷 색상은 이밀, 즉 강의 신을 상징했다. 강의 신은 감정, 직관, 물의 활기찬 생명력과 연관되어 있었다. 그러나 아키예브를 비롯한 일부 수도사들은 이밀이 변덕스럽고 우유부단하다고 주장했다. 조타가 이밀을 수호신으로 선택했을 때 장로들이 조타를 아키예브에게 맡겼던 것도 그런 이유였다. 장로들은 노련한 수도사인 아키예브의 엄격한 태도가 젊은 조타의 우유부단한 성격에 자극이 될 것이며, 반대로 아키예브도 조타의 자극을 받으리라 생각했다.</p>
	<p>“우리의 임무는 간단하며, 우리의 지침은 분명하다. 왜 망설임 때문에 임무와 지침을 흐트러뜨리느냐? 아키예브는 조타의 지팡이에 생긴 흠집을 살펴보며 말했다. “복종하지 않은 대가다. 의무에서 멀어지면 이런 결과를 얻을 것이다. 악한 바람이 불어올 때, 흔들리는 나무는 부러진다.”</p>
	<p>그날 있었던 기억을 되짚고 나자 달은 이미 중천이었다. 지팡이의 날카로운 흠집을 더듬은 탓에 엄지가 벗겨져 있었다. 소년은 아직 잠들어 있었다. 그 모습을 보자 조타는 화가 났다. 처음부터 만나지 않았더면 하는 생각이 들었다.</p>
	<p><em>‘소년이 문제가 아니야.’ </em> 조타는 혼자 중얼거렸다. 고아가 된 그 소년의 과거와 살해당한 야영지 사람들에 대한 생각이 마음을 어지럽혔다. 밤이 깊어갈 무렵, 조타는 결정을 내렸다. 이곳 남쪽에 마을이 있었다. 아직 마을이 멀쩡하다면, 그곳에 아이를 돌봐줄 사람이 있을 것이었다.</p>
	<p>만약 마을이 공격을 받아서 소년을 돌봐줄 만한 사람을 사흘 내에 찾지 못하면, 조타는 마지막 선택을 할 결심이었다. 그 선택은 안식이었다.</p>
	
