	<p>조타는 숲을 덮은 나뭇가지와 나뭇잎 사이로 뻗어 내려오는 한 자루 빛줄기 속에 서서, 새벽녘의 깨끗한 햇빛을 느끼고 있었다. 조타는 발가락 끝으로 선 채, 두 팔을 높이 들고 머리를 아래로 숙여 턱 끝이 가슴에 닿는 자세를 취했다. 그 상태로 눈을 감고 10분도 넘게 자세를 유지하며, 조용히 마음을 비우는 진언을 외웠다.</p>
	<p>아침에 행하는 명상은 스스로 허락한 명상 중 가장 휴식에 가까운 것이었다. 지난 몇 주 동안 거의 잠을 자지 못한 채 낮에는 여행을, 밤에는 경계를 서온 후였다.</p>
	<p>닷새가 지났고 소년은 아직도 살아 있었다. 조타의 우려대로, 찾아간 마을들은 비어 있기가 일쑤였다. 하루가 지날 때마다 조타는 소년을 신들께 보내지 못하는 구실을 만들었다. 오늘은, 멀지 않은 곳에 다른 마을이 있다고 스스로를 설득하며, 그동안 망설였던 자신의 결정을 정당화했다.</p>
	<p>“미시카에요... 이름요,” 소년이 조타의 평화로운 명상을 방해하며 말했다.</p>
	<p>“나는 조타라고 한다,” 조타는 퉁명스레 답하고는 다시 진언에 집중했다. </p>
	<p>잠시 후 조타는 낯선 소리를 들었다. 고르고라에서 들을 수 없는 감미로운 소리였다. 눈을 뜨자, 미시카가 플루트를 들고 떨리는 음을 연주하고 있었다.</p>
	<p>소년은 악기를 내려놓으며 물었다. “모스 배로스의 익살꾼이라는 노래 알아요?”</p>
	<p>“모른다,” 조타는 짜증을 내며 답했다. 사실 조타는 알고 있었다. 그 노래는 동요가 아니었다. 낯선 이름을 가진 영웅들이 가득한 노래, 조타 역시도 어린 시절 연주했을 법한 노래였다.</p>
	<p>“엄마가 좋아하는 노래예요. 우리가 무사했다면 아마 이 노래를 연주하셨을 거예요.” 미시카가 쓴웃음을 지었다. “내가 가르쳐줄게요.”</p>
	<p>“그럴 것&mdash;“ 조타가 말을 마치기도 전에 소년은 연주를 시작했다.</p>
	<p>조타는 한숨을 쉬고 명상 자세를 풀었다.	</p>
	<p><em>‘그리해서 기분이 좋아진다면 그냥 두자. 금방 끝날 테니,’ </em> 조타는 스스로에게 말했다.</p>
	<p>미시카와 함께 길을 떠나면서, 조타는 소년을 업었다. 이틀 전 밤 소년은 쓰러진 나무에 발이 걸려 넘어져 팔이 거의 부러질 뻔했다. 그 후 조타는 소년에게 부담을 주지 않으면서도 더 빨리 가기 위해 곧잘 미시카를 업어주곤 했다.</p>
	<p>조타가 깊은 숲 속으로 터덕터덕 발길을 옮기는 동안 아이는 계속해서 음악을 연주했다. 조타는 곧 연주를 그만두리라 생각하며 애써 무시하려 했지만, 오래지 않아 해가 질 무렵이 될 때까지도 소년은 플루트 연주를 멈추지 않았다.</p>
	<p>밤이 되어 조타가 새 야영지를 꾸리고 났을 때, 불현듯 음악에 대한 기억이 조타의 뇌리를 스쳤다. 기억 한 구석에서 왁자지껄한 웃음소리가 들렸고 맨발을 한 아이들이 초가지붕을 인 오두막집들 사이로 아무 걱정 없이, 세계의 질서와 혼돈 사이의 소중한 균형도 알지 못한 채, 순수한 마음으로 뛰노는 모습이 떠올랐다. 곧 조타는 그것이 자신의 어린 시절이었다는 걸 깨달았다.</p>
	<p><em>"악한 바람이 불어올 때, 흔들리는 나무는 부러진다." </em> 스승의 말이 머리를 울렸다.</p>
	<p>“그만!” 조타는 미시카가 들고 있던 플루트를 낚아채고 장식끈 춤에 넣었다.</p>
	<p>“그냥 들려주고 싶었을 뿐이에요,” 미간을 찌푸리며 소년이 말했다.</p>
	<p>“한 번으로도 족하다, 수백 번이나 그럴 필요 없다,” 조타는 투덜거리듯 말하며 화를 삭였다. 조타는 풀이 죽은 채 고개를 떨구는 미시카에게 말을 이었다. “밤이 어두우니 괜히 주의를 끌어서는 안 된다.”</p>
	<p>상황을 모면하려 한 말이었으나, 반 시간도 지나지 않아서 그 말은 사실로 드러났다.</p>
	<p>날카로운 두 차례의 휘파람 소리가 밤의 정적을 갈랐다. 조타는 마음의 눈으로 어떤 움직임이 있는지 숲을 살폈다. 신들은 그러나 조타를 인도하기를 꺼렸다. 곧, 숲 속에서 두 사내의 모습이 나타났다. 대강 맞춰 입은 갑옷은 전투의 흔적으로 닳아 있었다.</p>
	<p>조타는 한 눈에 그자들을 알아보았다.  <em>‘강도... 용병... 신들을 모르는 자들.’</em></p>
	<p>둘은 야영지 어귀에서 잠깐 망설이며 서로 표정을 살폈다. 그 중 두꺼운 근육질 팔뚝에 우람한 체격을 갖춘 사내가 왼쪽 귀에서부터 턱까지 패인 커다란 상처를 달빛에 드러낸 채 조타를 노려보더니, 몸을 돌려 자리를 뜨려 했다. 다른 사내가 그를 붙들었다. 깨끗이 수염을 민 잘생긴 얼굴이었으며 검은 장발을 어깨높이까지 기른 자였다. 모닥불에 비친 그의 녹색 눈은 미시카를 강렬하게 노려보고 있었다.</p>
	<p>“밤이 어둡습니다, 신성한 분이여,” 잘 생긴 사내가 시선을 거두고, 입을 열었다.</p>
	<p>“제 모닥불 빛이 당신을 편히 인도할 것입니다,” 조타가 고대의 인사말로 답했다. 이들에게도 조타는 여행자를 잘 살피라는 아키예브의 지시를 무시하지 않았다.</p>
	<p>“어인 일로 이런 깊은 산 속을 여행하십니까?” 두 사내가 모닥불 가에 자리를 잡고 있는 동안 조타가 물었다. 조타는 속으로 낯선 두 사내의 움직임을 주시하며 약점을 살폈으나, 호흡을 고르고 침착한 표정을 유지하며 아무런 티를 내지 않았다. 여행자들은 무장하고 있었다. 건장한 사내는 큼지막한 전투 도끼를, 또 다른 사내는 긴 칼을 등에 걸고 있었다.</p>
	<p>“당신과 마찬가지요.” 잘생긴 사내가 불에 손을 쪼이며 답했다. “수도사님들이 하실 일이 많은 모양이오. 수도단에서 도움을 받고자 무기를 가진 자들을 소집했소.”</p>
	<p><em>"거짓말"</em>, 조타는 그렇게 말하고 싶었으나 입을 다물었다. 장로님들이 신성한 뜻을 받들기 위해 강도들을 이용한다는 것은 신성 모독이나 마찬가지였다. 신들을 모르는 자들이 삶에서 귀하게 여기는 것은 단 하나, 황금뿐이었다.</p>
	<p>“장로님들께서 언제 그런 포고를 내리셨습니까?”</p>
	<p>“직접 그들을 만난 게 아니오. 이쪽 지역을 순찰하던 당신네 수도사 중 한 분에게 들었소. 이 숲에 악마가 한 마리 있다고 말이오. 작은 악마 새끼인데 장님 아이의 얼굴을 하고 다니고, 피부와 머리카락은 눈처럼 새하얗다더군.”</p>
	<p>미시카가 흥분하며 말했다. “난 악마가 아니야!”</p>
	<p>“그럼 왜 묶여 있는 거냐?” 상처가 있는 사내가 비웃었다.</p>
	<p>“날 쫓고 있는 놈이 악마예요. 엄마랑 다른 사람들을 죽였어요.” 미시카의 눈에서 피눈물이 고이기 시작했다. </p>
	<p>“피눈물이 아닌가...” 잘생긴 사내가 움찔하며 말했다. “악마가 아니면 저주를 받은 것이겠지.”</p>
	<p>“나도 어쩔 수 없어요. 태어날 때부터 그랬으니까. 엄마가 그랬어요. 저주라고 말하는 사람들은 바보들이라고.” 미시카는 묶인 손을 뻗어 조타를 더듬었다. “아저씨는 내 말 믿죠?”</p>
	<p>“조용히 해라,” 조타는 말하면서도 두려움과 망설임을 느꼈다.</p>
	<p><em>‘고르고라에서는 모든 것이 겉보기와 다르다.’ </em></p>
	<p>가능한 일이었다. 인정해야 했다. 수도단의 일부 어리석은 인원들은 용병의 도움을 요청하기도 했다. 만약 그 수도사가 이 소년을 두고 악마라 말했다면... 그동안 악마에게 속았다는 말인가?</p>
	<p>‘아니야.’ 조타는 며칠 동안이나 미시카를 봐왔다. 미시카는 신들에게 저주를 받았을지언정 그저 소년에 지나지 않았다. 물론 숲을 떠도는 위험한 어린아이에 대한 소문이 떠돈 건 사실이다. 그 수도사가 소문을 믿은 게 틀림없었다.</p>
	<p>“그 수도사는 어디에 계십니까? 소년에 대해 얘기를 나눠야겠습니다.”</p>
	<p>“악마에 대해 말이오?” 잘생긴 사내가 대답했다. “여기서 서쪽에서 본 게 마지막이오. 그 수도사가 우릴 찾았지, 우리가 찾아간 게 아니오.”</p>
	<p>“저 짐승을 내놓으시오,” 상처가 있는 사내가 끼어들었다. “우리가 악마를 찾아 데려가면 그 수도사 양반이 사례를 약조했소. 우린 돈이 필요하니까. 며칠 간 나무뿌리와 썩은 고기만으로 버티고 있소.”</p>
	<p>조타는 그 말을 무시했다. “서쪽이라 하셨습니까. 제가 찾아보겠습니다.”</p>
	<p>“같이 갑시다,” 덩치 큰 사내가 말했다. “우리 몫을 잊어선 안 되니까.”</p>
	<p>“당신들의 일은 끝났습니다.” 조타가 일어나 미시카를 일으켜 세웠다.</p>
	<p>“그렇다면 당신이 돈을 내놓으시겠소?” 잘생긴 사내가 물었다.</p>
	<p>“장로님들의 감사가 보답이 될 겁니다.”</p>
	<p>덩치 큰 사내가 조타의 발 앞에 침을 뱉었다.</p>
	<p>다른 사내가 한숨을 내쉬며 말했다. “자, 조금 오해가 있는 것 같소만, 의무나 명예나 다 좋은 게 아니겠소. 당신 같은 머리털 없는 양반들한테는 말이오. 그런데 우리는 그렇지 않다오.”</p>
	<p>조타는 몇 차례 호흡을 가다듬으며 화를 삭였다. 이들을 너무 오래 참아주었다. “그래서 당신 같은 자들이 부패와 치욕 속에서 사는 것이오.”</p>
	<p>얼굴에 상처가 있는 사내는 표정이 험악해졌으나, 동료 사내는 웃기만 했다. 경멸과 오만함이 묻어나는 웃음이었다. 그자는 계속 킬킬거리며 등에서 킨 칼을 뽑아들었다. </p>
	<p>“고집깨나 있으시군, 안 그렇소?” 그 사내가 물었다. “당신 수염은 우리가 만난 다른 수도사보다 훨씬 짧소. 산속 오두막에서 장로님 찌찌나 빨아 먹던 때가 그리 오래되지 않은 게지.”</p>
	<p>온 근육이 들썩이는 와중에도 조타는 미동도 하지 않고 말했다. “신들을 모르는 자 둘을 너무도 오래 상대해주었구나.”</p>
	<p>“흠, 둘이라 했나? 셋은 아니고?” 잘생긴 사람이 휘파람 소리를 냈다.</p>
	<p>조타의 뒤쪽 어둠 속에서 강철 화살촉이 박힌 나무 화살이 날카롭게 공기를 가르는 소리가 들려왔다. 조타는 몸을 돌려 지팡이로 반원을 그리며, 화살을 튕겨냈다. 몸에 거의 닿을 뻔한 한 뼘 정도의 거리였다.</p>
	<p>다시 몸을 야영지 쪽으로 돌렸을 때, 잘생긴 사내는 모닥불을 건너 미시카에게로 달려가고 있었다. 조타는 지팡이를 모닥불 쪽으로 겨누었다. 지팡이에서 공기의 파장이 흘러나와 모닥불에 충돌했다. 불에 탄 장작이 뭉텅이로 강도를 향해 날아갔다. 불길에 휩싸인 장작 대부분이 갑옷을 스치며 떨어졌으나 불똥 하나가 강도의 얼굴을 지나 오른쪽 눈에 들어갔다. 사내는 고통 속에서 비명을 질러댔고 불길은 퍼져 나가 머리카락을 불태웠다.</p>
	<p>덩치 큰 사내가 모닥불을 뛰어넘어 머리 위로 전투 도끼를 들어 올린 채 조타에게 달려들었다. 커다란 도끼가 눈앞까지 떨어지는 와중에도 조타는 꿈적도 하지 않았다. 마지막 순간에, 조타는 옆으로 비켜서며 불경스러운 공격을 피했고, 적의 도끼는 땅바닥 깊이 박혔다. 조타가 도끼를 쥔 사내의 팔을 지팡이로 내리치자, 두 팔은 깨진 포도주병처럼 부서진 뼛조각과 피로 난장판이 되었다.</p>
	<p>활시위를 놓는 희미한 소리가 조타의 뒤에서 들려왔다. 조타는 황급히 몸을 숙였고, 화살은 조타의 어깨를 스쳐지나 덩치 큰 사내의 가슴팍에 명중했다. 보이지 않는 적이 욕을 내뱉는 소리가 들렸고, 뒤이어 발소리가 들리더니 야영지 반대편의 더 깊은 숲 속으로 사라져갔다.</p>
	<p>조타는 주위를 살폈다. 잘생긴 사내는 목과 얼굴이 피와 물집으로 뒤범벅된 채 죽어 있었다. 그런데 미시카가 보이지 않았다.</p>
	<p>“미시카?” 조타는 미시카를 불렀다. 날카로운 공포가 그를 엄습해왔다.</p>
