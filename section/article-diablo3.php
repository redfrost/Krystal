	<p><em>"악한 바람이 불어올 때, 흔들리는 나무는 부러진다."</em></p>
	<p><span class="dropcap">조</span>타는 아키예브의 마지막 말을 잊을 수 없었다. 그 말은 지난 몇 주간, 매 걸음 조타의 뒤를 따라다녔다. 해가 지기 전까지는 기억 속 스승의 목소리가 작게 들려왔으나, 밤이 되면서 커다란 울림으로 들려왔다.</p>
	<p>오늘도 그런 밤이었다... 조타는 오늘 밤도 시험에 들리라는 걸 알았다.</p>
	<p>바람이 거세어지더니, 고르고라에 굉음이 울려 퍼졌다. 죽어가는 신이 마지막으로 뱉어낸 차디찬 숨결 같은 바람이었다. 찬 기운이 초록과 하양과 파랑이 어우러진 장식띠를 뚫고 뼛속까지 파고들었다. 한때 하늘수도원 바깥에서 휘몰아치던 산바람도 흐트러짐 없이 견디어냈던 조타였지만 이 바람은 그와는 달랐다. 이 바람은 왠지 모를 다급한 기운이 서려 있었고 이것이 그를 불안하게 했다. 숲의 신들이 두려움에 아우성치는 듯한 느낌이었다.</p>
	<p>조타는 야영지 바깥쪽에서 규칙적으로 발걸음을 옮기며, 지팡이로 이끼투성이 땅을 짚어나갔다. 높다랗게 솟은 이끼 낀 소나무와 자작나무들이 조타가 밤을 지낼 야영지를 둘러싸고 있었고, 유독 오래되어 보이는 떡갈나무 고목이 한 그루 서 있었다. 고목에서 뻗어나온 커다란 옹이투성이 가지들이 야영지를 보호하듯 덮어주고 있었다.</p>
	<p>두 사내는 조타가 지펴놓은 모닥불 옆에서 해진 양모 이불을 돌돌 말고 잠들어 있었다. 조타는 조용한 밤을 보내고 싶었지만, 해가 떨어지자마자 피난민 두 명이 찾아오는 바람에 소망이 수포로 돌아갔다. 피난민들에게 자리를 내주고 싶지 않은 생각도 들었지만, 여행자들을 돌려보내지 말라는 스승님의 가르침을 거역해서는 안 되었다.</p>
	<p>“두 팔 벌려 여행자를 맞이하되, 마음으로는 만일을 대비해라.” 아키예브는 그렇게 말했다. “혼돈의 신에게 더럽혀진 티가 있는지 조심스럽게 살펴라. 만약 그렇다면 여행자들이 어떻게든 네 시선을 피할 테니까.”</p>


	<p>조타는 스승의 말대로, 낯선 피난민들을 면밀히 살폈다. 곧, 그들이 타락하지 않았다는 사실을 알 수 있었다. 여윈 몸에 피곤한 눈을 한 두 사내는 늙어가는 아버지와 스무 살 아들로, 야만적인 카즈라 떼의 공격에서 유일하게 살아남은 이들이었다. 비열한 카즈라 염소인간들은 부자가 살던 마을을 불시에 습격했고, 마을을 무덤이나 다름없는 쑥대밭으로 만들어버렸다.</p>
	<p>아버지와 아들은 종교적으로 그리고 문화적으로 이브고로드와 이어진 고르고라 출신으로, 고르고라 시의 안전한 북쪽 지역으로 피난을 가던 길이었다. 극심한 공포를 마주한 뒤였지만, 두 부자는 희망에 차 있었으며 조타를 만난 것을 운명의 신이 굽어살피는 징조라고 생각했다. 조타는 그들이 이브고로드의 벽 안에서 살아갈 이야기를 나누는 걸 들으며 운명의 잔혹함을 느꼈다. 그들은 이브고로드에 도착하기 전에 죽을 운명이었다.</p>
	<p>잠들 준비를 하면서, 두 부자는 야영지를 허락해준 데 대한 답례로 가지고 있던 마지막 식량 꾸러미를 건넸다. 조타는 예의상 받고 싶은 시늉을 해 보이고는 음식을 사양했다. 사실 피난민들과 엮이고 싶은 마음이 없었다. 조타는 고르고라에서 만난 이들과 가깝게 지내서는 안 된다는 걸 알고 있었다. 장애물이 될 수도 있기 때문이었다. </p>
	<p>“그럼 대신 신들께 두 번 공물을 바치겠소.” 괜찮다는 듯, 아버지가 말했다. “성스러운 분이시여, 고맙게도 신들께서 우리를 당신에게 인도하셨으니 말이외다. 고르고라에서는 모든 것이 겉보기와 다르다오.”</p>
	<p><em>‘물론입니다.’</em> 조타는 대답하고 싶었다. <em>‘저도 그렇습니다.’</em></p>
	<p>노인의 말이 숲을 짚은 것이라면 그 말은 사실이었다. 조타는 어려서부터 이브고로드의 남쪽, 고르고라 원시림에 관한 이야기를 들었다. 수도단이 생겨날 당시, 고르고라 숲에서 비교적 어린 나무조차도 고목이라 불릴 만큼 오래된 거목이었다. 가르침에 따르면, 일천 하고도 한 명이나 되는 질서와 혼돈의 신들이 이곳에서 이루는 조화는 변할 수 없는 것이었다. 장로 수도사님들이 어둠의 도가니가 되어버린 이 숲의 모습을 본다면 어떤 말씀을 하실지 궁금해졌다.</p>
	<p>조타는 진언 하나를 반복해서 읊조리며 야영지를 계속 돌았다. 눈으로는 볼 수 없었던, 주위 숲의 모습이 마음으로 보이기 시작했다. 어둠 속에서 움직임이 일었다. 밤 초입에 알아챘던 그 무언가의 존재였다. 느리고, 체계적인 움직임이었다. 야영지를 향해 다가오기라도 하듯이 매시간 그 존재감이 강해졌다. 모습을 감춘 그 무언가가 사방에서 백 개나 되는 눈을 통해 자신을 쳐다보는 듯한 느낌에 조타는 소름이 돋았다. 더 불길한 것은 그 존재의 정체를 보여달라는 조타의 기원에 답을 해준 숲의 신이 아무도 없었다는 사실이었다. 신들은 냉담했다... 믿을 수 없었다.</p>
	<p>그 사건이 있고서 신들은 계속 이런 식이었다. 몇 주 전, 불덩이 하나가 이브고로드의 하늘을 가로질러 왕국 남쪽의 어딘가에 떨어졌다. 그 불덩이와 함께, 혼돈의 신들과 악마의 종자들이 숲을 배회하기 시작했고, 강도들은 손쉽게 고르고라의 외딴 마을들을 수탈해갔다. 유성의 이름과 정체에 대해서 갖은 추측이 나돌았다. 하지만 십수 가지가 넘는 모든 가설에서 공통된 이야기가 하나 있었다. 바로 악의 시기가 도래했다는 것이었다. 조타를 둘러싼 산속의 울창한 숲은 그 어둠이 가장 짙게 스며든 곳이었다. 유성의 진정한 정체를 알아내는 것은 조타의 임무가 아니었다. 조타 역시도 존중해 마지않는 수도단의 다른 뛰어난 수도사 하나가 그 임무를 맡아, 하늘에서 떨어진 불덩이의 정체를 찾아 길을 떠났다.</p>
	<p>밤이 깊어가자, 조타는 불안해졌다. 숲 속에 도사린 알 수 없는 부정한 힘이 자신을 조롱하는 듯 여겨졌다. 조타는 자신이 직접 지팡이에 새겨 넣은 수백 자나 되는 문구를 손가락으로 한 자 한 자 더듬었다. 글자는 지팡이의 끝에서 시작하여 뱀처럼 몸통을 휘감아 다른 쪽 끝까지 복잡한 무늬를 이어갔다. 한 자 한 자가 수련을 통해 얻은 교훈이었다. 조타는 글귀를 반복해서 읊으며, 답을 얻거나 결의를 다질 수 있기를 바랐다. 그러나 정작 떠오르는 것은 아키예브의 가르침을 받는 동안 좌절했던 기억뿐이었다.</p>
	<p>조타가 나지막이 글귀를 읊고 있었을 때 바람이 휘파람 소리만큼 작게 잦아들었다.</p>
	<p>멀리서, 마른 장작이 타는 듯한 따닥거리는 소리가 터져 나와 고르고라에 울려 퍼졌다. 곧 또 다른 소리가 연이어 터져 나왔다. 수상한 그 소리는 처음에는 희미하고 잘 들리지 않았지만, 점차 커지더니 반복이 잦아지며 야영지 주위 모든 곳에서 들려오기 시작했다. 조타는 눈에 신경을 집중하고 어둠 속을 응시했다. 소리는 더욱 커져, 가지 부러지는 소리와 나무가 쪼개지는 소리가 뒤섞인 굉음이 되었다. 야영지 너머로 나무들이 줄줄이 몸을 비틀고 있었고 불이 옮겨붙을 때마다 폭발음과 함께 더해진 불길이 커다란 화마가 되어 야영지 쪽으로 다가오는 모습이 보였다.</p>
	<p>그 움직임은 야영지 바로 바깥에서 멈췄다. 죽음과 같은 정적이 숲에 내려앉았다.</p>

