 	<p>노인과 아들이 몸을 일으켜 세웠다. 아직 잠에 취해 있었다.</p>
	<p>무슨 일이오?” 노인이 중얼거리며 물었다.</p>
	<p>조타는 손가락을 들어 보이며 노인에게 말을 아끼라는 시늉을 했다. 조타는 어둠 속으로 기어들어갔다. 아무런 움직임도 형체도 없는, 혼돈의 신들이 보낸 존재들이 가득 느껴지는 칠흑의 어둠이었다. 비록 볼 수는 없었지만, 아주 가까이에 있었고 손을 뻗치면 당장에라도 닿을 듯이 느껴졌다. 그 존재는 모든 곳에 있었다. 흙에도, 공기에도, 나무에도. </p>
	<p><em>'나무에도'</em></p>
	<p>조타가 발아래 기척을 느낀 순간 땅이 불룩 솟아올랐다. 거대한 나무뿌리가 위로 들쳐지더니 조타의 몸이 공중을 날았다. 아래로는 축축한 흙이 빗줄기처럼 쏟아졌다. 조타는 땅에 떨어지며 몇 바퀴 굴러간 후, 야영지의 다른 편에서 발을 디디고 일어났다.</p>
	<p>주위 나무들이 휘청거렸다. 가지가 길게 뻗쳐 나왔고 삐걱거리며 낮은 신음 소리를 내는 모양이 마치 세월을 잊은 잠에서 깨어난 거인들 같았다. 희미한 야영지 모닥불 빛에 움직임이 비쳤다. 수많은 뿌리가 땅을 헤집고 솟아올라 조타와 피난민 부자를 향해 채찍질을 해대기 시작했다.</p>
	<p>“모닥불에서 떨어지지 마십시오!” 조타는 피난민들에게 소리쳤다.</p>
	<p>두 피난민 부자는 몸을 웅크리고 모닥불에서 나뭇가지를 골라내더니, 급조한 횃불을 무기 삼아 야영지 중앙으로 다가오는 뿌리들을 향해 흔들었다. 조타는 발밑에서 공격해왔던 뿌리의 몸통을 찾아내어, 근처 소나무를 향해 달려갔다. 조타는 지팡이를 휘두르며 질풍과 같은 공격을 소나무에게 퍼붓고, 나무 몸통에 손바닥을 대고 일격을 날렸다. 조타의 손바닥이 닿은 자리 주위가 쪼개지고, 균열은 위쪽까지 퍼져 나갔다. 조타는 뒤로 물러서서 부서지는 나무의 잔해 속에서 밑동이 드러나는 걸 보았다. 위쪽 가지는 바로 옆 자작나무에게로 고꾸라졌다.</p>
	<p>소나무가 쓰러졌지만, 조타는 그 안에 있던 악이 사라지는 것을 느끼지 못했다. 그 부정한 존재는 힘이 그저 줄어들기만 했다. 조타는 마음의 눈으로 야영지를 감싸는 나무들을 살폈다. 모두 타락해있었지만, 나무들을 조종하는 원흉은 하나일 것이었다.</p>
	<p>조타의 시선이 떡갈나무 고목에 꽂혔다. 떡갈나무는 죽은 듯 미동조차 없었다. 세월에 시들고 말라버린 나무껍질 속에서 조타는 순간 주위 나무에 영향력을 행사하는 악의 존재를 느꼈다.</p>
	<p>조타가 그 존재를 알아채자, 떡갈나무는 몸통을 비틀며 이끼 거품이 가득한 커다란 주둥이를 드러냈다. 그 입에서 날카로운 비명이 튀어나왔다. 그 소리는 밤의 정적을 갈랐고, 조타는 무릎에 힘이 풀리고 말았다. 피난민 부자는 땅에 주저앉으며 귀를 싸매고 고통스러운 비명을 질러댔다.</p>
	<p>주위 나무들의 움직임이 잦아들었다. 악마가 힘을 한데 모으자 다른 나무들이 떡갈나무에게 힘을 돌려주었다. 야영지 곳곳에서 커다란 나뭇가지들이 흔들거리며 땅을 향해 내려왔다. 마치 뾰족한 날이 선 창 수십 개가 움직이는 것 같았다. 조타는 옆으로 몸을 피해 지팡이로 큰 반원을 그리더니 공기로 만들어낸 투명한 칼날을 옹이투성이 나뭇가지들에 날려보냈다. </p>
	<p>떡갈나무가 분노에 찬 비명을 지르더니, 남아 있는 가지로 공격을 재개했다. 조타는 공중에서 공격해오는 나뭇가지를 넘어 공중제비를 돌아서 떡갈나무의 밑동에 착지했다. 단 한 번의 공격에 힘을 집중하며 지팡이를 나무의 아가리에 찔러 넣었다. 온 정신을 지팡이 끝 한 점에 집중시켰다.</p>
