	<p>장로들은 신들의 목소리 그 자체였다. 정의로움의 귀감이었다. 누구든 미시카 같은 아이를 낳는다면, 아무리 너그럽게 생각해도 흉조임을 부인할 수 없었다. 이런 험악하고 흉흉한 시기에 그런 아이를 낳았다는 사실은 장로의 순수성을 의심받기에 충분했다. 어머니와 몇 명의 충직한 하인들의 도움으로 미시카는 목숨을 부지하고 이브고로드에서 몰래 빠져나와 고르고라의 깊고 깊은 숲 속으로 들어오게 되었다.</p>
	<p>조타가 이야기를 끝냈을 때, 아키예브는 한참 동안 조타를 지켜보며, 이야기에 반박하거나 질문을 던지지 않았다. 그저 “저 악마가 전해준 거짓말만을 들었구나.”라고만 대꾸했다.</p>
	<p>“상상조차 하기 힘든 일입니다만, 아이가 거짓말을 한다고 생각하지 않습니다.”</p>
	<p>“<em>믿느냐?</em> 수도단의 일원인 네 명예를 걸고 그 이야기가 사실이라고 맹세하겠느냐?”</p>
	<p>“그렇습니다,” 조타는 대답했다. 확신하지 못하는 목소리였다.</p>
	<p>아키예브는 고개를 숙이고 숨을 깊이 들이쉬었다. “그렇다면 내가 틀렸다…”</p>
	<p>“말씀하신 대로입니다. 고르고라에서는 모든 것이&mdash;“</p>
	<p>아키예브는 돌려차기로 조타의 가슴팍을 내리치며 말을 끊었다. 조타는 폐에서 공기가 빠져나가는 것을 느꼈다.</p>
	<p>온 세상이 까맣게 변했고 머릿속에서 종소리가 울려 퍼졌다. 저 너머로 미시카의 비명이 들려왔다. 다시 눈을 떴을 때 아키예브의 모습이 희미하게 보이기 시작했다. 소년의 머리를 잡아챈 채였다.</p>
	<p>“내가 너를 잘못 봤다,” 아키예브가 내뱉었다. “어찌 이 정도까지 벗어날 수 있느냐? 악마와 그 변장한 모습을 내게 알려준 것은 바로 장로님들 중 한 분이다! 감히 장로님께 의문을 가질 셈이냐?”</p>
	<p>조타는 땅에 지팡이를 대고 힘을 다해 일어섰다. 그때, 굴하지 않는 자의 말이 조타의 뇌리를 때렸다. <em>‘장로님들 중 한 분이라니. 다른 여덟 분의 장로님들은 이 임무에 관여하시지 않았다는 말인가?’'</em></p>
	<p>“저것을 죽여라,” 굴하지 않는 자가 명령했다. “그럼 네 죄를 용서받을 것이다.”</p>
	<p>따르고 싶은 마음이 간절했다. 그 오랜 시간을 스승의 가르침에 따라 살아왔고, 스승의 말씀을 거스른다는 생각만으로도 병을 얻을 정도였다. 그러나 조타의 마음속 깊은 곳에서는 명령을 따르지 말아야 한다는 속삭임이 들려왔다. 직관이었다. 통찰의 불빛이었다. 수련하는 과정 내내 아키예브가 지워버리라고 늘 이야기하던 그것이었다. 옳다고 배웠던 모든 것들에 반하는 것이었으나, 그 불빛은 설명할 수 없는 방식으로 진실과 함께 빛나고 있었다.</p>
	<p>“악마가… 아닙니다…” 조타는 숨을 헐떡이며 겨우 말을 이었다.</p>
	<p>스승이 한숨을 내쉬었다. “네가 강하길 바랐다. 그래서 네 내면의 약점을 극복할 수 있기를 말이다. 그러나 넌 아직도 어린아이로구나. 네가 이렇게 된 것도 내 잘못이다.”</p>
	<p>“신들이 불안해합니다. 스승님 말씀대로입니다.” 조타는 모독이 될 수도 있는 다음 말을 생각하며 마음을 다잡았다. “스승님에게 임무를 맡긴 장로는 균형을 유지하는 것에 관심이 없습니다.” 조타는 계속 말했다. “스승님께서 찾으시는 악마는, 만약 존재한다면, 지금도 활개를 치고 있을 것입니다.”</p>
	<p>아키예브가 무릎으로 조타의 배를 가격했고, 조타는 땅에 나뒹굴었다. 조타가 고개를 들었을 때 아키예브의 손바닥이 번쩍하며 다가왔다. 이마에서부터 고통이 전해졌다. 따뜻하고 축축한 무언가가 조타의 눈으로 흘러들었고 코를 지나 떨어졌다. 아키예브가 손을 빼고 피묻은 무언가를 손에서 털어냈을 때 조타는 그것이 자신의 이마에 새겨졌던 질서와 혼돈의 문양임을 알아보았다.</p>
	<p>“너는 이 문양을 가질 자격이 없다! 넌 수도사가 아니다... 당장 수도원으로 돌아가서 날 기다리거라. 장로님들께서 네 불손한 행동을 아셔야 할 것이다.”</p>
	<p>굴하지 않는 자가 미시카를 끌고 걸어나갔다. 조타는 수치심 속에서 몸을 일으켜 세웠다. 자신의 지팡이에 새긴 실패와 교훈의 글귀를 만질 때마다 손이 불타는 듯한 느낌이 들었다.</p>
	<p>분노... 아키예브에게 패배한 그 모든 순간을 생각하며, 조타는 분노에 타올랐다. 조타가 스스로를 믿고자 했을 때마다, 굴하지 않는 자는 그를 얕잡아보기만 했었다. 온몸에 분노가 퍼져 나갔다.</p>
	<p>조타는 아키예브에게 달려갔다. 거리를 좁히고 지팡이를 옆으로 휘둘러 스승의 목을 겨누었다. 조타는 마치 단단한 바위를 친 것처럼 두 팔이 얼얼해지는 것을 느꼈다. 지팡이가 휘어지더니 기다란 금이 생기고 지팡이 몸통 전체가 갈라지기 시작했다. </p>
	<p>아키예브가 잠깐 휘청거리는 틈을 타 미시카가 몸을 피했다.</p>
	<p>“숨어라! 어머니께서 말씀하신 대로!” 조타가 소리쳤다. “어머니 노래가 들리기 전에는 나오지 마라!” 미시카가 비틀거리며 숲 깊은 곳으로 들어갔다. 혼자서는 그러나 멀리 가지 못하리라는 것을 조타는 알고 있었다.</p>
	<p>그러나 아키예브는 미끼를 물었다. 검을 빼들고 소년을 뒤쫓았다. 숲 속까지 빛이 잘 들지 않았던 탓에 칼은 희미하게 빛났다. 조타는 지팡이를 들어 굴하지 않는 자의 가슴을 공격했다. 아키예브는 가볍게 지팡이를 쳐내고 조타의 다리를 겨누어 빛처럼 빠른 속도로 검을 휘둘렀다. 조타는 공격을 피해 뒤쪽 나무에 발을 디딘 후 노 수도사의 머리 너머로 몸을 던졌다.</p>
	<p>굴하지 않는 자의 검이 지나자 나무 밑동이 한 번에 잘려나갔다. 높다란 소나무가 공터에 있는 짐꾼 짐승 쪽으로 기울기 시작했다. 짐승이 울음소리를 내며 뛰쳐나갔고 등에 매달린 짐가방이 나뭇가지에 걸려 땅에 떨어졌다. 소나무 몸통이 땅에 떨어지고 천둥과 같은 소리가 울려 퍼지자 조타는 움찔했다.</p>
	<p>아키예브의 물건이 사방팔방으로 흩어졌다. 가장 큰 가방이 찢어져 열렸고, 그 안에서 무언가가 빠져나오더니 소금과 약초가 쏟아진 곳까지 굴러가 멈추었다. 부패한 살점과 창백한 피부, 검은 머리카락이 엉겨 붙어 있었다.</p>
	<p>여자의 머리였다. 크게 벌린 입이 마치 소리 없는 비명을 지르다 굳어버린 모습이었다.</p>
	<p>수수께끼의 조각이 맞춰졌다. 학살당한 교역단. 머리가 잘린 시체. 악마.</p>
	<p>조타는 아키예브를 바라보았다. 믿고 싶지 않았다. 스승은 여러 모습을 지닌 존재였다. 아마도 수도단에서 가장 잔인하고 엄격한 수도사이리라. 그러나 살인자라니. 생각조차 못해본 일이었다.</p>
	<p>조타는 장로님들이 그런 도륙 행위를 용인하리라고 상상하기 힘들었다. 어떤 이유에서도 할 수 없는 일이었다. 그렇다. 잘못된 일이었다. 미시카의 아버지인 장로가 혼돈과 서약을 맺고 다른 장로들의 동의 없이 혼자 행동한 것이리라. 아키예브를 선택한 것도 그런 까닭일 것이다. 아키예브라면 한 치 망설임도 없이 주어진 임무를 수행할 사람이었다. 아키예브는 굴러떨어진 머리를 다시 쳐다보지 않았다.</p>
	<p>아키예브가 긴 칼로 조타의 왼쪽 알통 부위를 내리쳤고, 정확하게 팔 근육을 갈랐다. 조타의 팔이 아래로 쳐졌다. 조타는 불규칙한 걸음걸이로 노 수도사로부터 몇 발짝 물러난 후 몸을 추슬렸다.</p>
	<p>조타는 아키예브의 머리를 겨누어 다른 한 손으로 지팡이를 휘두르는 척하고, 굴하지 않는 자의 배를 발로 올려 찼다. 아키예브는 조타의 발목을 붙잡아 쓰러진 나무를 향해 내던졌다.</p>
	<p>조타가 안전하게 착지하기도 전에, 아키예브는 자리에서 뛰어올라 조타에게 검을 내리쳤다. 조타는 오른팔로 지팡이를 휘둘러 공격을 받아내려 했으나, 자신의 상대가 그 전설적인 존재라는 생각에 급격히 위축되고 말았다. 수련 도중 그랬던 것처럼 마음속에 의구심이 일었다. 검은 조타의 지팡이를 갈랐지만, 공격을 받아내려던 방어 동작을 취한 덕분에 노 수도사의 공격이 빗겨가고 말았다. 아키예브의 긴 칼은 조타의 가슴을 사선으로 갈랐고, 그 자리에 얕은 상처를 남겼다.</p>
	<p>조타는 오른팔에 기대어 일어서려 했으나, 고통과 패배감에 사로잡혀 다시 주저앉고 말았다.</p>
	<p>“내가 생각한 대로 싸웠구나, 품위도 없고 결의도 없었다.” 아키예브가 말했다.</p>
	<p>“아시지 않습니까. 그 소년은 악마가 아닙니다.” 조타가 간신히 물었다.</p>
	<p>“난 장로님의 명령을 알 뿐이다. 의문은 품지 않는다.”</p>
	<p>“교역단... 스승님이 그들을 죽이셨습니다.”</p>
	<p>“의무를 수행했다.”</p>
	<p>“신을 모르는 자들의 손을 빌려야 하셨습니까? 죄 없는 자들을 죽이기 위해서?”</p>
	<p>“그 강도들은 그저 도구일 뿐이다. 내가 성스러운 분들의 도구이듯이 말이다. 만일 그자들이 내게 악마를 데려왔다면, 난 그들 역시 신들의 심판에 맡길 생각이었다. 교역단 사람들은 그 괴물을 숨겨주었다. 괴물이 어디로 도망갔는지 내가 물었을 때, 그자들은 장로님들을 욕되게 말했다. 그 여행자들은 개처럼 죽었다. 그들은 개였다.”</p>
	<p>아키예브는 잘려나간 머리를 가리키며 말했다. “저 머리는 여자 악마의 것이다. 악마를 죽인 증거로 내가 가져왔다. 악마 소년의 노예였지. 악마가 새로운 희생자들을 꾀어낼 요량으로 마을로 들여보낸 여자였다.”</p>
	<p>“거짓말입니다.” 조타는 대답했다. “소년의 아버지, 그 장로는 공포에 굴복하여 살인을 저질렀습니다. 사람들이 자신을 타락했다고 여길까 두려워한 것입니다. 만약 그런 아이를 낳았다는 사실이 알려진다면, 사람들이 자신을 몰아내려 들지도 모른다 생각하신 것입니다. 스스로의 목적을 위해 균형의 체계를 버리셨습니다.”</p>
	<p>“의무를 따른다는 것이 무엇인지 너는 알지 못한다.” 아키예브가 응수했다. “넌 신들의 뜻을 행하는 나를 인간의 마음으로 비난한다. 넌 이단자만도 못하다. 넌 내 명예와 전 수도단의 명예에 오점을 남겼다. 널 신들께 보내 심판을 받게 해주겠다.”</p>
	<p>“그저 소년일 뿐인 걸 아시지 않습니까? 스승님은 진실을 외면하고 계십니다.” 조타가 말했다. 굴하지 않는 자는 검을 머리 위로 들어 올렸다. 스승의 눈에서 아주 잠깐 망설임의 눈빛이 비쳤다.</p>
	<p>아키예브는 개의치 않고 검을 휘둘렀다. 시간이 느려지는 것 같았다. 강철의 검이 공기를 가르며 아래로... 아래로... 아래로... 내려왔다. 갑자기 조타는 망설이는 것이 자신이 아닌 아키예브임을 깨달았다. 굴하지 않는 자가 스스로의 나약함을 내보이며 몰아치는 혼돈 앞에서 몸을 굽히고 진실에 눈을 감았다.</p>
	<p>조타는 침묵을 지키고 있는 주변의 신들에게 힘을 달라고 기원했다. 고르고라에 순수한 무언가가 남아 있다면 그 존재는 미시카일 것이다. 조타는 이 한 가지 생각에만 집중하며, 스스로가 균형의 원리를 지키고 있다는 사실을 다시 떠올렸다. 조타는 공포와 고통을 진정시키고, 오른손 손바닥에 정신을 집중하여 검을 막을 정도로 강해지기만을 빌었다.</p>
	<p>굴하지 않는 자의 검이 조타의 손에 부딪혔다. 검은 산 천제를 올려놓은 듯 무겁게 그를 짓눌렀다. 그러나 검의 날은 조타의 손바닥을 베지 못했다. 조타는 아키예브처럼 흔들리지 않을 것이었다. 무너지지 않으리라 생각했다.</p>
	<p>“악마가 아닙니다.” 조타는 손가락으로 검을 움켜쥐며 쏘아붙였다. “이제라도 정의를 따를 기회가 있습니다!”</p>
	<p>"<em>닥쳐라!</em>" 노 수도사가 소리쳤다. 눈썹에 구슬땀이 맺힌 채, 조타의 손에서 검을 빼내려고 애를 쓰고 있었다. 그럴 수 없다는 사실을 깨달았을 때, 굴하지 않는 자는 몸을 숙여 조타의 손에 검을 밀어 넣었다.</p>
	<p><em>‘흔들리지 않을 것이다. 무너지지 않는다.’</em></p>
	<p>조타는 야수와 같은 함성을 내지르며 손목을 비틀었다. 아키예브의 검이 죽은 나무토막처럼 부러졌고, 노 수도사는 갑자기 풀려버린 장력을 감당하지 못하고 휘청거렸다. 조타는 부러진 검날을 손 안에서 옆으로 뉘인 후 단단히 움켜쥐고, 스승의 목을 베었다. 너무나 부드럽게 베인 나머지 아키예브의 머리는 목 위에 머물러 있었고, 곧 아키예브의 몸이 고꾸라졌다.</p>    

    </section>
    	
    <section id="page8" class="story-page tab-pane fade">	
    <small class="page-number">8</small>


	<p>조타는 얼마나 오랫동안 누워 있었는지 기억하지 못했다. 숲의 나뭇잎 사이로 보이는 구름 한 점 없는 하늘처럼 깨끗한 마음으로 위를 올려다보고 있었다. 그 후 자신이 무슨 일을 했는지도 기억나지 않았다. 조타는 상처를 싸맸고, 치유의 진언을 읊었으며, 왼팔에 조금씩 힘이 돌아오자 장작더미를 쌓아 아키예브의 시신을 정화했다. 처음으로 조타가 기억해낸 것은 플루트를 입에 가져갔던 순간이었다. 처음에는, 어렸을 때 연주했던 노래를 기억하지 못할 것 같았다.</p>
	<p>그러나 맞게 연주한 것이 분명했다. 미시카가 공터로 나타났기 때문이었다.</p>
	<p>“조타?” 소년이 부드러운 어투로 물었다.</p>
	<p>“그래.”</p>
	<p>미시카가 소리를 따라와 조타의 옆에 섰다.</p>
	<p>“그 악마는...”</p>
	<p>“그 분은 악마가 아니다. 어쨌든 지금은 죽은 몸이야.” 조타가 답했다.</p>
	<p>조타는 미시카의 손을 묶은 장식띠를 풀어주었고 어머니의 머리가 있는 곳으로 아이를 데려갔다. 신들께 돌려보내기 전에 미시카에게 작별 인사를 할 기회를 주고 싶었다. 그러나 미시카는 “아뇨... 필요 없어요. 노래가 있으니까요.”라고만 말했다.</p>
	<p>일을 마무리한 후 조타는 어디로 가야 할지를 생각했다. 아키예브가 미시카를 죽이고 증거를 가져오라는 임무를 수행하지 못했다는 사실을 안다면 명령을 내린 장로가 어떻게 나올지 알 수 없었다. 게다가, 굴하지 않는 자와 같은 수도사를 또 하나 찾는 것은 거의 불가능한 일이라는 것도 조타는 알고 있었다. 균형이라는 본질을 거스르면서까지 부정한 살육과 파괴를 이의 없이 수행할 수도사는 없었다.</p>
	<p>최근 엄청난 일들을 알게 되었지만 조타는 아키예브와 그 장로가 일탈자라는 사실에 위안을 삼았다. 고르고라에 대한 말처럼, 그들은 이 세계에 찾아온 어려운 시기를 증명하는 증거였고 교정할 수 있는 부정함이었다. 다른 수도사들, 아키예브가 했던 일들을 하지 않은 그 명예로운 전사들은 목숨을 걸고 움트는 혼돈의 세력을 몰아내고 있었다. 그들은 수도단 설립의 기초가 된 정의로운 신념에 눈을 감지 않았고 조타도 그럴 것이었다.</p>
	<p>조타는 미시카의 손을 잡고 공터를 벗어나서 북쪽 이브고로드로 향했다. 수도단에 가서 일어난 일들을 모두 밝힐 생각이었다. 갈 길이 지금처럼 분명했던 때는 없었다. 처음으로, 조타는 수도사가 된다는 것의 의미를 진정으로 이해한 기분이었다.</p>
	</div>
	
