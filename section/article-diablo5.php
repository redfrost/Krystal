    <section id="page2" class="story-page tab-pane fade">                           
    <small class="page-number">2</small>
    
	<p>이센드라가 한 소녀를 앞세우고 내 거처에 들이닥쳤다. 물과 불처럼 다른 한 쌍이었다. 우아한 녹색 장포를 입고 금 장신구로 치장한 이센드라는 화려하고 당당한 모습이었지만, 함께 있는 소녀는 주위에 있는 것들에 매료되어 고개를 이리저리 돌리는 모습이 마치 작은 새 같았다. 소녀의 눈길은 선반 위의 책에서 이상한 액체나 가루가 담긴 병들, 나조차도 쓰임새를 알 수 없는 마법 기구들 사이를 재빠르게 움직였다. 옷은 낡을 대로 낡은 데다가 땀과 먼지에 얼룩져 넝마나 다를 바 없었다. 칼데움 시장바닥에서 부유한 상인들에게 매달리는 거지 아이 중 하나라고 해도 믿었으리라. 헝클어진 길고 검은 머리카락은 건조하고 푸석푸석해진 채, 몸과 마찬가지로 먼지와 진흙이 엉켜 붙어 떡이 되어 있었다. 피부는 햇빛에 갈색으로 그을리고, 입술은 터지고 갈라져 있었다. </p>
	<p>"그래, 이 아이냐?" 이센드라 앞에 선 더러운 아이를 쳐다보며 내가 물었다.</p>
	<p>이센드라는 못마땅한 듯 소녀를 훑어보며 대답했다. "안뜰에서 발견했습니다. 마티즈, 알레른, 탈리야와 결투를 벌이고 있더군요." 목소리에서 언짢은 기색이 느껴졌다. "모두 기꺼이 도전을 받아들였지요."</p>
	<p>"이 아이는 멀쩡해 보이는데." 나는 말했다. "다른 학생들은?"</p>
	<p>"마티즈와 알레른은 치료를 받고 있습니다. 탈리야는 자존심에 상처를 받았을 뿐이고요."</p>
	<p>소녀는 이야기를 들으며 씩 웃었다.</p>
	<p>"잘된 일일지도 모르지." 나는 말했다. "겸손이란 미덕을 좀 배울지도 모르니 말이다. 그 아이들과는 나중에 얘기하지."</p>
	<p>"하지만 나랑은 지금 얘기하겠지, 늙은이." 소녀가 말했다. 어린아이의 확신이 담긴 거만하고 또렷한 목소리였다. </p>
	<p>"저게 말을 하는구나." 나는 이센드라를 보며 웃음 지었다.</p>
	<p>"그러게요." 이센드라가 딱딱하게 말했다. "말 참 예쁘게도 하네요."</p>
	<p>"당신은 누구지?" 소녀가 따졌다. "날 왜 여기로 데려왔지?"</p>
	<p>"나는 발데크다. 비제레이 대의원이자 이샤리 성소 마법단의 수장이지."</p>
	<p>소녀는 오랫동안 침묵을 지키며 나를 훑어보았다. </p>
	<p>"당신이?" 마침내 소녀가 입을 열었다.</p>
	<p>나는 웃음을 터트렸다. "말해 보아라, 아이야. 넌 누구고 왜 여기 왔지? 우리 수습생을 병실에 보내는 것보단 원대한 목적이 있어서 왔을 게 아니냐?"</p>
	<p>"내 이름은 리밍이다. 그리고 난 아이가 아냐." 소녀가 말했다. "난 <em>마법사</em>다."</p>
	<p>"대담한 선언이구나." 나는 말했다. 놀라움을 숨기려고 좀 애써야만 했다. 마법사, 역사상 가장 악명 높은 마법학자들에게 붙여진 이름. 보통 사람들은 공포를 느끼고, 마법에 익숙한 자들도 두려워하는 이름을 들먹이는 소녀라니. </p>
	<p>"허풍이 아니야." 리밍의 목소리에 위험한 기색이 담겨 있었다.</p>
	<p>나는 소녀를 진정시키려 손을 들었다. "그럼 보여 다오."</p>
	<p>말이 채 끝나기도 전에 거센 돌풍이 내 책상 위에 몰아쳐 종이, 책, 잉크병 따위를 휩쓸어 가더니, 끝내는 마구잡이로 바닥에 내동댕이쳐 거대한 무더기를 만들었다. 내 표정은 변하지 않았고, 소녀는 이를 더 실력을 보이라는 신호로 받아들였다. 리밍은 두 팔을 양옆으로 뻗고, 위를 향한 손바닥에 두 개의 불길을 불러냈다. 천장을 향해 혀를 날름거리는 불기둥이 소녀의 갈색 눈동자 속에서 춤을 추었고, 갑자기 불어닥친 뜨거운 공기에 머리카락이 마구 흩날렸다.</p>
	<p>나는 어깨를 으쓱했다. "요술쟁이의 장난이로군."</p>
	<p>리밍은 분한 듯 이를 악물었다. 소녀가 주먹을 쥐자 불길이 사라졌지만, 열기는 여전히 남아 있었다. 팔을 다시 움직이자 눈부시게 밝은 붉은색과 주황색 불길이 내 책상 한가운데 나타나, 마치 살아 있는 양 너울거리며 춤을 추었다. 다시 팔을 흔들자 책장에 꽂혀 있던 내 책들이 튀어나와 공중에 떠다녔다. 책들은 일렬로 방을 가로지르더니 회오리바람에 휩쓸린 것처럼 리밍을 휘감고, 하나씩 바닥에 쌓여 왕좌를 이뤘다. 소녀는 그 위에 앉아 나를 쳐다보았다. </p>
	<p>리밍이 한쪽 눈썹을 들어올리자 나는 천천히, 침착하게 박수를 쳤다.</p>
	<p>"그게 전부냐, 얘야?" 나는 물었다. 내가 같잖다는 듯 손을 흔들자 책상 위의 불길이 꺼지고 소녀가 앉아 있던 책 왕좌가 와르르 무너졌다. 리밍은 넘어지기 전에 발딱 일어났다. "사람들은 마법사라고 불리는 마법학자들을 두려워했지. 몇 번이고 이 세계를 멸망 직전으로 몰고 간 자들, 길들여지지 않은 엄청난 힘을 멋대로 휘둘러 땅이 요동치게 한 자들 말이다. 그들은 불타는 지옥의 악마들과 거래하고, 우리 모두를 멸망으로 이끌겠다고 서약했지. 죽음을 모면하고 창조의 질서를 뒤흔들었어. 넌 그저 한 노인의 물건을 엉망진창으로 흩트리고, 책상 위에 불을 피웠을 뿐이다." </p>
	<p>"더 엄청난 것도 할 수 있어." 소녀는 방어적으로 말했다. "언젠가 난 가장 위대한 <em>마법사</em>가 될 거라고."</p>
	<p>"내 경험상 그 '언젠가'가 오는 데에는 꽤 오랜 시간이 걸릴 수 있더구나. 막상 그때가 됐을 때 뜻대로 안 될 수도 있고."</p>
	<p>"헤론 강 계곡에서 벌어진 기적 얘기 들었어?" 소녀가 물었다.</p>
	<p>"거기서 무슨 일이 있었단 얘길 듣긴 했다. 가뭄을 해결하려고 애쓴 한 여자아이 이야기였지." 나는 무뚝뚝하게 말했다. "사람들이 그 아이를 마법사라고 불렀던 것 같구나."  </p>
	<p>"내가 그 마법사야." 리밍이 자랑스럽게 말했다. "몇 달이나 비가 오지 않았고 헤론 강은 거의 말라붙었지. 온 들판이 갈색으로 말라 가고 있었다고. 계곡 사람들은 여러 신에게 비는 수밖에 없다고 생각했어. 하지만 난 신들이 하지 않을 일을 할 수 있었지."</p>
	<p>"함부로 신들을 모독하는 건 현명하지 못한 행동인데." 나는 말했다.</p>
	<p>소녀는 내 말을 무시했다. "난 물을 찾아봤어. 땅속 깊은 곳에 있는 웅덩이에서 물을 끌어올리고, 갈라진 강바닥에 겨우 남은 물을 모았지. 그렇게 모은 물을 바람에 실어 폭풍을 일으키려고 했어. 처음엔 아무 일도 벌어지지 않았고, 사람들은 나더러 팔을 휘저으며 비가 오라고 기도하는 멍청한 소녀라고 했지. 하지만 난 알고 있었어. 몇 시간이 지나자 맑은 하늘이 어두워졌지. 엷은 회색 구름이 갑자기 나타나서 수평선 너머까지 뻗치고, 태양을 가릴 만큼 짙어졌어. 비를 잔뜩 머금고 밤처럼 검은 색깔이 돼서 계곡에 그늘을 드리웠지. 날 비웃던 사람들도 믿기 시작했어. 사방에서 천둥 소리가 울려 퍼지고 구름 속에서 번개가 번쩍였다고. 공기가 축축해지고, 산에서 안개가 밀려 내려오면서 피부로 물기를 느낄 수 있었어. 안개는 보슬비가 되고, 보슬비는 소나기가 되고, 마침내 폭우가 쏟아졌지. 그 물이 땅에 모두 흡수되고, 헤론 강에 다시 물이 흐르기 시작했어. 난 그런 일도 할 수 있다고."</p>
	<p>이센드라는 못 믿겠다는 얼굴이었다. "그런 일을 할 수 있는 아이는 없다."</p>
	<p>"당신이 못 한다고 해서 나도 못한다고 생각하지는 마." 자기보다 스무 살은 더 많은 마술사에게 리밍이 대꾸했다.</p>
	<p>"나도 처음엔 너처럼 의심했지." 나는 이센드라에게 말했다. "지금은 실제로 무슨 일이 벌어졌는지 안다. 저 애가 말한 대로야. 몇몇 세부적인 사항은 빼먹었지만 말이다."</p>
	<p>리밍은 여전히 당당하게 고개를 쳐들고 있었지만, 얼굴에서 미소가 사라졌다. </p>
	<p>나는 말을 이었다. "비가 오고 나서 또 가뭄이 닥쳤지. 전보다 더 심하게 말이야. 사람들은 비를 부른 마법사를 비난하며 모든 책임을 그녀에게 돌렸어."</p>
	<p>리밍이 다시 입을 열었다. 목소리가 약해져 있었다. "그렇게 날 칭찬하던 사람들이 날 쫓아내야 한다고 했어. 엄마 아빠는 동의했지. 난 도우려고 한 것뿐이야. 그런 일이 벌어질 줄은 몰랐어."</p>
	<p>"사람들은 마법학자를 믿지 않는다. 이해하지 못하니까 두려워하지. 이샤리 성소에서 훈련 받은 마법학자라면 그런 일에 어떤 위험이 따르는지 알았을 게다." 난 소녀에게 웃음 지었다. "하지만 그 마법학자들이 네가 한 일을 시도한대도, 네 반만큼이라도 성공할지는 의문이구나."</p>
	<p>리밍은 내 태도가 변한 걸 잽싸게 알아차렸다. "그럼 날 가르쳐 줘."</p>
	<p>"그런 생각도 해보았다만, 네 역량만 알 뿐 여기 학생이 될 수 있을지는 잘 모르겠구나. 배울 것도 많지만, 배운 것 중 버려야 하는 건 더 많아. 그리고 네가 그걸 끝까지 견뎌낼 의지가 있는지도 의심스럽고."</p>
	<p>"어떻게 그런 말을 할 수 있지? 당신 수습생들보다 내가 더 강해. 다 데려오라고. 직접 보여줄 테니까! 원한다면 당신이랑도 싸울 수 있어, 늙은이. 상관없다고. 난 여기서 공부하려고 바다를 건너고 사막을 헤치며 왔고, 여기서 공부할 거야."</p>
	<p>"그건 네 마음대로 정할 수 있는 게 아니다. 결정은 내가 하는 거지." 나는 말했다.</p>
	<p>"제가 가르칠게요." 이센드라가 불쑥 말했다.</p>
	<p>"뭐라고?" 난 물었다.</p>
	<p>리밍은 의혹이 서린 눈초리로 이센드라를 쳐다보았다.</p>
	<p>"이 계집애에겐 뭔가 있어요. 스승님 말씀대로 결실을 얻지 못할지도 모르지만, 제 눈에도 이 아이의 잠재력이 보입니다. 언젠가 이 아이가 필요해져서, 오늘 돌려보낸 걸 후회하게 될지도 모르죠." 이센드라는 미소 지었다. "예전의 제 모습이 좀 생각나기도 하고요."</p>
	<p>리밍은 고개를 흔들었다. "당신은 싫어. 저 사람한테 배울 거야."</p>
	<p>이센드라가 아이를 쏘아보았다. "고마운 줄을 알아야지. 네 부모가 널 가져볼까 생각만 하고 있을 때부터 난 전쟁터에서 지옥의 군주들과 맞서 싸우고 있었어. 건방진 어린 것에게 마법이나 가르치려고 지금껏 수련한 게 아닌데도 널 맡아 주겠다는 거라고."</p>
	<p>"거절하겠어." 리밍이 말했다.</p>
