	<p>며칠 후 깨어났을 때 리밍은 이미 도시를 떠난 뒤였고, 그녀가 어디로 갔는지 아는 이는 아무도 없었네. 성소 사람들은 무슨 일이 벌어졌는지 숨길 수가 없었다고 얘기했지. 칼데움 어디에서나 성소에서 피어오르는 연기를 볼 수 있었고, 깨지고 흩어진 돌덩이 때문에 밖에서도 우리의 전투가 남긴 흔적을 똑똑히 볼 수 있었으니까.</p>
	<p>마법사에 대해 내가 아는 것은 여기서 끝이며, 이제 결정만이 남았네. 과거에 마법학자들이 세상을 위협했을 때, 한 비제레이 군주가 암살단을 만들었지. 우리가 너무 강해져 온 세상을 위험에 빠뜨리지 않도록 하는 마법사 사냥꾼들. 그 비제레이 군주는 지금 내 자리에 서서, 지금 우리가 이야기하듯 첫 번째 암살자와 이야기했네. 그리고 많은 위대한 마법학자들이 죽음을 맞았지. </p>
	<p>내가 그런 결정을 내리는 건 이번이 두 번째로군.</p>
	<p>자네를 보내 이센드라를 감시한 사람이 나란 걸 리밍도 알았다고 믿네. 그리고 그게 무엇을 의미하는지 알면서도, 리밍은 날 살려주었네. 내가 이센드라에게 그리했듯 자신에게도 죽음을 선고할지 모른다는 걸 알면서. </p>
	<p>하지만 이건 알아 주게. 리밍은 거짓말을 하지 않았네. 우리 도서관에는 지금 벌어지고 있는지도 모르는 일련의 사건이 기록된 고서가 실제로 있어. 모든 일은 천상에서 떨어지는 별과 함께 시작되고, 내가 리밍과 싸운 날 바로 그런 별이 떨어졌지. </p>
	<p>나는 마법이 진정 어떠한 것인지, 그리고 내가 어떤 사람인지 잘 안다네. 리밍도 이를 알지만, 그녀는 다른 길을 가게 되었지. 이것이 우리 앞에 놓인 문제라네, 암살자. 내 눈에도 슬금슬금 손길을 뻗치는 악이 보이지만, 리밍이 이를 막기 위해 과연 어떤 행동을 취할지 두려워. 그래서, 내 가장 영리한 제자이자 어쩌면 이 세상의 가장 큰 희망일지 모를 그녀에게 죽음을 선고하네. 내 선택이 옳기를 빌면서.</p>
	<p>하지만 난 바로 이 방에서 내 앞에 섰던, 두려움이라곤 전혀 모르는 소녀를 기억하네. 선한 일을 해서 남을 돕고 싶어하던 처녀를, 날 올려다보며 가르침을 구했던 여성을 기억하네. 그녀에겐 어떤 과업도 너무 무겁지 않았고, 가능성이 없는 일이란 존재하지 않았지. </p>
	<p>그녀는 자신의 선택을 했고, 나 또한 나의 선택을 했네. </p>
	
	</section>


