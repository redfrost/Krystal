    <small class="page-number">1</small>
    	
	<p><span class="dropcap">부</span>디 양해해 주길 바라네. 그 마법사에 대해 할 말이 많다네. 그녀의 이야기를 전부 해줄 수 있는 건 나뿐이지. 이건 내가 져야 할 짐일세. 앞으로의 일도 그렇고. 결말은 이미 짐작하고 있겠지. 우릴 둘러싼 무너진 벽과 깨진 돌덩이, 모든 이의 입에서 흘러나오는 소문들을 보면 쉬이 알 수 있는 일 아닌가. </p>
 
	<p>하지만 마법이란 것과 관련해서는 그 무엇도 간단하지 않다네. 자네가 보고 들은 게 이야기의 전부는 아닐 수 있다는 걸 알아 두게나. </p>
	<p>의사들은 내가 살 거라고 단언했지. 몸을 회복하려고 이렇게 침대에 누워만 있자니, 희미해지는 과거의 기억을 더듬으며 이 끔찍한 재난을 예고하는 조짐은 없었는지 생각하는 것 이외엔 어차피 할 것도 별로 없더군. 난 누구보다도, 심지어 그녀 자신보다도 그녀를 잘 안다네. 본인은 결코 인정하지 않겠지만 말이야. 그녀는 우리 시대 가장 강력한 마법사일 걸세. 순수한 마음에, 선한 일을 하겠다는 바람밖에 없지. 하지만 젊음이의 어리석음과 천재의 자만심에 사로잡혀 있어. 어기지 못할 규칙이 없고, "할 수 없다"나 "해서는 안 된다"는 말 따위는 영영 이해하지 못할 걸세. 몇 해 전 처음 만날 때부터 그랬어.</p>
	<p>꼭 오늘 같은 날이었지.</p>
	
