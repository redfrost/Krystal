	<p>나는 손을 들어올려 가벼운 마력을 흘려 보내, 성소에서 나가는 문을 닫았다. 하나씩 하나씩 문은 모두 닫히고 우리는 전당 안에 고립되었다.</p>
	<p>"그렇다면 널 막아야만 하겠구나." 나는 장포의 긴 소매를 꼼꼼히 접어 올렸다. "넌 내 최고의 제자다, 리밍. 시간이 지나면 네가 내 자리를 이어받아 마법단을 이끌리라 생각했다. 네가 날 능가하리라 믿었지. 이렇게 돼서 유감이구나. 어쩌면 실패한 건 나일지도 모르지."</p>
	<p>"스승님께서는 좋은 스승이셨어요. 실제로 전 많은 것을 배웠죠. 하지만 스승님께서는 우리가 받은 재능을 결코 이해하지 못하실 거예요. 그게 제가 스승님을 능가할 이유고요." 리밍은 말했다. 목소리가 전당 안에서 메아리쳤다. </p>
	<p>그녀가 집중하면서 눈이 가늘어지는 것이 보였다. 우리가 몸 주위에 마력을 모으자 높은 횃대에 꽂힌 횃불들이 깜박거렸다. 우리는 강 한가운데에서 꼼짝도 하지 않는 두 개의 바위처럼 마주보고 서 있었다. 리밍은 양 옆으로 손을 뻗치고 손가락을 구부렸다. 나는 지팡이를 몸 앞에 낮게 들고 마력을 집중했다.</p>
	<p>"궁금했던 적 없으세요, 스승님? 제가 스승님보다 강하진 않을지." 그녀가 물었다.</p>
	<p>"아니." 나는 미소 지었다. "그런 적 없다." </p>
	<p>나는 리밍이 먼저 행동하길 기다렸다. 그녀는 활활 타오르는 구슬을 몇 개 만들어냈다. 구슬은 횃불에서 빛을 빨아들이고 바깥에서 들어오는 빛을 희미하게 만드는 것 같았지만, 눈이 어둠에 익숙해지고 나니 그저 착시 효과일 뿐이었다. 리밍은 타오르는 구슬을 내게 던졌다. 나는 구슬을 밀어내고 바닥에 내동댕이쳤다. 구슬은 대리석을 태웠지만 내게는 닿지 않았다. 공기가 뜨거워졌고, 나는 호흡이 가빠지는 것을 느꼈다. 리밍은 놀란 표정으로 날 쳐다보았지만 바로 다음 공격을 준비했다. 천장에서 커다란 돌덩이들을 뜯어내 불을 붙이고, 내가 서 있는 곳에 떨어뜨렸다. 나는 지팡이를 머리 위로 들어올리고 바깥쪽으로 마력을 내뿜어, 희미하게 빛나며 점점 커지는 구체를 만들었다. 유성처럼 떨어지던 돌덩이는 막에 부딪혀 먼지가 되었고, 개중 큰 조각은 바닥에 튕겨졌다. 반투명한 보호막은 나를 지켜 주었지만, 그 반향은 내 몸을 고통스럽게 훑고 지나갔다. 젊은 시절에는 이렇게까지 힘들지 않았겠지만 지금은 저절로 무릎이 후들거렸다. 뜯겨 나온 대리석 타일이 내 주위에서 깨진 거울 조각처럼 튀어, 리밍도 뒤로 물러났다. </p>
	<p>"그거보단 잘해야 할 텐데." 나는 말했다.</p>
	<p>리밍은 이를 악문 소리를 냈지만, 이번에는 손바닥에서 시시각각 색이 변하는 가느다란 광선을 내뿜었다. 내가 할 수 있는 거라곤 무시무시한 낫처럼 나를 겨누고 다가오는 광선을 피하는 것밖에 없었다. 광선에 스친 돌은 칼에 잘린 것처럼 깨끗하게 갈라졌다. 광선은 대리석 바닥판을 마구 조각 냈고, 곧 바닥 전체가 무너져 내릴 것만 같았다. 나는 손을 바깥쪽으로 뻗어 금방이라도 바스러질 듯한 돌덩이들을 보이지 않는 힘의 줄기로 고정시켰다. 내가 이 힘을 거두어들이면 바닥이 꺼지고 나 또한 떨어질 터였다. 주 전당 아래에는 단단한 대지가 아닌 지하 묘지가 자리 잡고 있었고, 그곳에 떨어져도 내가 살아남을 것 같지는 않았다. 온 바닥을 지탱하는 건 엄청나게 힘들었고, 나는 마디가 하얗게 되도록 지팡이를 꽉 움켜쥐었다. </p>
	<p>리밍은 내 주위에서 전당 바닥이 갈라지고 부서지는 것을 보았다. 그녀는 손을 움직였고, 순간 내 발밑의 돌은 흔적도 없이 흩어졌다. 예전에 이센드라에게 기술을 하나 배운 적이 있는데, 무심결에 나는 그 기술을 쓰기 시작했다. 한순간 나는 무너지는 타일 위에 서 있다가, 다음 순간에는 몇 미터 떨어진 곳을 조금 더 자신 있게 내딛고 있었다. 비록 짧은 거리였지만 순간이동의 고통은 어마어마했다. 내가 수천 조각으로 찢어졌다가, 불타는 실로 다시 봉합되는 기분이었다. 어느 쪽이 더 고통스럽다 말하기도 어려웠다. 리밍은 내가 발 디딘 곳을 차례차례 파괴했고, 나는 다시 움직였다. 이 괴상한 춤은 한동안 계속되었지만 내 반응은 횟수를 거듭할수록 느려졌다. 이 전투가 내 늙고 허약한 육신을 갉아먹는 것이 느껴졌다.</p>
	<p>나는 바닥에 지팡이를 꽂았고, 그 지점에서부터 천둥이 우르릉 쾅쾅 울려 퍼졌다. 눈 깜짝할 사이에 번개가 전당 안에 내리치고, 번개가 꽂힌 곳에서는 폭발이 일어나 대리석 바닥판 조각이 사방으로 튀었다. 번개는 큰 소리를 내며 터져 나와 리밍을 향했다. 그러나 그녀에게 꽂히지는 못했다. 삐죽빼죽한 빛줄기는 리밍이 팔을 벌리고 집중하자 공중에서 얼어붙었다. 나는 단념하지 않고 계속 번개를 소환했고, 폭풍은 점점 더 심해졌다. 번개는 리밍 위에서 부채꼴로 퍼져나갔고 결국 그녀도 더는 막을 수 없었다. 번개는 리밍 주위에서 전기를 튀기며 그녀를 바닥으로 밀어붙였고, 눈부신 흰 빛을 뿜으며 폭발했다. </p>
	<p>리밍이 사라졌다.</p>
	<p>그녀의 의도를 짐작하지 못한 채 나는 폭풍에 불을 붙였다. 전기는 성난 불길로 바뀌어 주 전당을 채웠고, 내 피부도 불에 그슬려 안 그래도 지친 몸이 더욱 힘겨웠다. 리밍이 다시 시야에 나타났을 때, 그녀는 불길에 휩싸여 있었다. 고통에 비명을 지르는 소리가 들렸다. 리밍에게 다가가는 내 발밑에서 바닥판이 흔들렸다. 바닥이 무너지지 않게 하는 주문에 계속 주의를 기울이면서, 나는 그녀의 뒤틀린 몸에 지팡이를 겨누었다. </p>
	<p>리밍 앞에 선 채, 나는 내 무게를 지탱해 주는 단단한 땅에 안도감을 느꼈다. </p>
	<p>"넌 아직 배울 게 많다, 리밍."</p>
	<p>나는 지팡이를 뻗었지만 순식간에 리밍은 사라졌고, 지팡이는 그녀의 몸 대신 허공을 찔렀다.</p>
	<p>겨우 몸을 돌려 내 뒤에 있던 그녀를 마주보았다. 입을 열어 주문을, 아무 주문이라도 외우려고 했지만 폭발이 내 시야를 뒤흔들었다. 나는 발밑의 바닥을 지탱해 주는 주문을 놓치고 말았다. 바닥은 크게 들썩이며 흩어졌고, 모든 것이 떨어져 내렸다. 나는 암흑 속으로 떨어지고 또 떨어져, 마침내 지하 묘지의 차가운 돌바닥에 털썩 부딪혔다. </p>
	<p>만신창이가 된 몸으로 누워 있으니 불과 먼지 냄새가 몰려왔다. 리밍이 둥실 뜬 채 위에서 내려와 내 옆에 무릎을 꿇었다. </p>
	<p>그녀는 말했다. "스승님께서는 제가 스승님의 가르침을 배우지 못했다고 생각하시지만, 전 배웠어요. 이센드라의 죽음에서 배웠죠. 하지만 전 이 능력은 이유가 있어서 주어진 거라고, 이 능력을 펼치는 게 제 의무라고 생각해요. 저는 제 힘을 스승님처럼 두려워하지 않고, 사용할 겁니다."</p>
	<p>"네가 그 힘을 통제하지 못하면 어쩌느냐?" 목소리가 갈라졌다. "네 힘으로 세상을 파괴할 수도 있어."</p>
	<p>"그러면 온 세상이 눈물을 흘리겠지요." 그녀는 뒤돌아섰다. "여쭙고 싶은 게 하나 더 있습니다, 스승님."</p>
	<p>나는 침묵했다. 무슨 질문인지 알고 있었다. 리밍이 내게 얻어낼 것은 그것밖에 남아 있지 않았으니까. </p>
	<p>"이센드라는 왜 죽었나요? 진실을 얘기해 주세요." 그녀는 말했다.</p>
	<p>"나도 너만큼밖에 모른다."</p>
	<p>리밍은 끄덕이고는 밖으로 나갔다.</p>
	<p>나는 다시 말을 하려고 입을 열었지만, 곧 어둠이 모든 것을 집어삼켰다.</p>
	
    </section>
    	
